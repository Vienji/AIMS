    private void choosePhotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_choosePhotoActionPerformed
        
        try {
            JFileChooser chooser = new JFileChooser();
            chooser.showOpenDialog(null);
            File file = chooser.getSelectedFile();
            String filePath = file.getAbsolutePath();
            itemFilePath.setText(filePath);
            ImageIcon icon = new ImageIcon(filePath);
            Image image = icon.getImage().getScaledInstance(itemPhoto.getWidth(), itemPhoto.getHeight(), Image.SCALE_SMOOTH);
            itemPhoto.setIcon(new ImageIcon(image));   
        } catch (NullPointerException e) {}
              
    }//GEN-LAST:event_choosePhotoActionPerformed

    private void addItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addItemActionPerformed
        
        if(itemName.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Please input the item name!");
        } else if ( (int) quantity.getValue() <= 0 ) {
            JOptionPane.showMessageDialog(rootPane, "Quantity must be more than 0!");
        } else if (costPerItem.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please input the cost per item!");
        } else if (checkNumber(costPerItem.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Enter a numeric number!");
        } else {
            int n = JOptionPane.showConfirmDialog(rootPane, "Are you sure that all of the data that you've written are correct?");
            
            if( n == 0 ){              
                
                new InventoryDBController().addStock(itemName.getText(), (String) supplier.getSelectedItem(), String.valueOf((int)quantity.getValue()), 
                        costPerItem.getText() , description.getText(), encoder.getText(), itemFilePath.getText(), 
                        new InventoryDBController().itemStatusChecker((int)quantity.getValue()));
                
                JOptionPane.showMessageDialog(rootPane, "Item was successfully added to database!");
                
                itemPhoto.setIcon(null);
                itemFilePath.setText("");
                itemName.setText("");
                quantity.setValue(0);          
                description.setText("");
                costPerItem.setText("");
                supplier.setSelectedIndex(0); 
            }
             
        }
        
              
    }//GEN-LAST:event_addItemActionPerformed

